function getIcon(name) {
  for (const [re, icon] of ICON_MAP) if (re.test(name)) return icon;
  return '🍞';
}
function getBg(name) {
  for (const [re, , bg] of ICON_MAP) if (re.test(name)) return bg;
  return '#F9FAFB';
}

// ─── Canvas Export ────────────────────────────────────────────────────────────
function drawExport(canvas, { items, stockMap, branchName, empName }) {
  const W = 800;
  const LINE_H = 46;
  const HEADER_H = 160;
  const FOOTER_H = 72;
  const H = HEADER_H + LINE_H + items.length * LINE_H + FOOTER_H;
  canvas.width = W;
  canvas.height = H;
  const ctx = canvas.getContext('2d');
  const F = (size, bold) => `${bold ? 'bold ' : ''}${size}px "Sarabun","Tahoma","Arial",sans-serif`;

  ctx.fillStyle = '#FFFFFF';
  ctx.fillRect(0, 0, W, H);

  // Header strip
  ctx.fillStyle = '#4A2E1A';
  ctx.fillRect(0, 0, W, 100);

  ctx.fillStyle = '#E8C89E';
  ctx.font = F(28, true);
  ctx.textAlign = 'center';
  ctx.fillText('☕ JE BAR Coffee & Pastry', W / 2, 38);
  ctx.fillStyle = '#FFFFFF';
  ctx.font = F(20, true);
  ctx.fillText('ใบเช็คสต็อคขนม', W / 2, 68);
  ctx.font = F(14);
  const dateStr = new Date().toLocaleDateString('th-TH', { year: 'numeric', month: 'long', day: 'numeric', weekday: 'long' });
  ctx.fillText(`${dateStr}  |  สาขา: ${branchName}  |  ผู้เช็ค: ${empName}`, W / 2, 92);

  // Sub-header bar
  ctx.fillStyle = '#F9F5F0';
  ctx.fillRect(0, 100, W, 60);
  ctx.fillStyle = '#6B4226';
  ctx.font = F(13);
  ctx.textAlign = 'left';
  const openCount = items.filter(i => (stockMap[i.id] || 0) > 0).length;
  ctx.fillText(`รายการทั้งหมด: ${items.length} รายการ  |  มีสินค้า: ${openCount} รายการ  |  ไม่มีสินค้า: ${items.length - openCount} รายการ`, 32, 136);

  // Table header
  const y0 = HEADER_H;
  ctx.fillStyle = '#4A2E1A';
  ctx.fillRect(0, y0, W, LINE_H);
  ctx.fillStyle = '#FFFFFF';
  ctx.font = F(14, true);
  ctx.textAlign = 'left';
  ctx.fillText('#', 24, y0 + 30);
  ctx.fillText('รายการขนม', 68, y0 + 30);
  ctx.textAlign = 'right';
  ctx.fillText('จำนวน (ชิ้น)', W - 32, y0 + 30);

  // Rows
  items.forEach((item, idx) => {
    const ry = y0 + LINE_H + idx * LINE_H;
    const qty = stockMap[item.id] || 0;
    ctx.fillStyle = idx % 2 === 0 ? '#FFFFFF' : '#FAF7F3';
    ctx.fillRect(0, ry, W, LINE_H);

    // Divider
    ctx.strokeStyle = '#E5DDD5';
    ctx.lineWidth = 1;
    ctx.beginPath();
    ctx.moveTo(0, ry + LINE_H - 0.5);
    ctx.lineTo(W, ry + LINE_H - 0.5);
    ctx.stroke();

    // Row number
    ctx.fillStyle = '#9CA3AF';
    ctx.font = F(13);
    ctx.textAlign = 'left';
    ctx.fillText(String(idx + 1), 24, ry + 30);

    // Item name
    ctx.fillStyle = '#1F2937';
    ctx.font = F(15, true);
    ctx.fillText(`${getIcon(item.name)}  ${item.name}`, 68, ry + 30);

    // Qty
    ctx.textAlign = 'right';
    if (qty > 0) {
      ctx.fillStyle = '#166534';
      ctx.font = F(18, true);
    } else {
      ctx.fillStyle = '#9CA3AF';
      ctx.font = F(15);
    }
    ctx.fillText(qty > 0 ? String(qty) : '—', W - 32, ry + 30);
  });

  // Footer
  const fy = y0 + LINE_H + items.length * LINE_H;
  ctx.fillStyle = '#4A2E1A';
  ctx.fillRect(0, fy, W, FOOTER_H);
  ctx.fillStyle = '#E8C89E';
  ctx.font = F(13, true);
  ctx.textAlign = 'center';
  const totalQty = items.reduce((s, i) => s + (stockMap[i.id] || 0), 0);
  ctx.fillText(`รวม ${totalQty} ชิ้น  |  Generated by LUCID HR · JE BAR`, W / 2, fy + 42);
}

async function exportToImage({ items, stockMap, branchName, empName }) {
  const canvas = document.createElement('canvas');
  drawExport(canvas, { items, stockMap, branchName, empName });
  return new Promise(resolve => canvas.toBlob(resolve, 'image/png'));
}

// ─── Helpers ──────────────────────────────────────────────────────────────────
function fmtTime(ts) {
  const d = new Date(ts);
  return d.toLocaleString('th-TH', { month: 'short', day: 'numeric', hour: '2-digit', minute: '2-digit' });
}

